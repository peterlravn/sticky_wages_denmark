function [lhs, rhs] = static_resid(y, x, params)
T = NaN(5, 1);
lhs = NaN(12, 1);
rhs = NaN(12, 1);
T(1) = (1+params(2))/(params(5)+params(2)+(1-params(5))*params(1));
T(2) = (1-params(5))/(1-params(5)+params(5)*params(6))*(1-params(3))*(1-params(3)*params(4))/params(3);
T(3) = (1-params(8))*(1-params(4)*params(8))/(params(8)*(1+params(2)*params(7)));
T(4) = T(3)*(params(1)+params(2)/(1-params(5)));
T(5) = (-1)/params(1);
lhs(1) = y(1);
rhs(1) = params(4)*y(1)+params(5)*T(2)/(1-params(5))*y(3)+T(2)*y(6);
lhs(2) = y(2);
rhs(2) = params(4)*y(2)+T(4)*y(3)-T(3)*y(6);
lhs(3) = y(3);
rhs(3) = y(3)+T(5)*(y(9)-y(1)-y(10));
lhs(4) = y(3);
rhs(4) = y(4)-T(1)*y(11);
lhs(5) = y(4);
rhs(5) = y(11)+(1-params(5))*y(5);
lhs(6) = y(6);
rhs(6) = y(7)-y(8);
lhs(7) = y(8);
rhs(7) = (1-params(5)*T(1))/(1-params(5))*y(11);
lhs(8) = y(6);
rhs(8) = y(6)+y(2)-y(1);
lhs(9) = y(10);
rhs(9) = y(11)*T(1)*(-params(1))*(1-params(9));
lhs(10) = y(9);
rhs(10) = y(1)*params(11)+params(12)*y(12)+y(9)*params(10)+x(1);
lhs(11) = y(12);
rhs(11) = y(4)-(y(4));
lhs(12) = y(11);
rhs(12) = y(11)*params(9)+x(2);
end
