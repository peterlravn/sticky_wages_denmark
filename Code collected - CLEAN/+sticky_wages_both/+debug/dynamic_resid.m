function [lhs, rhs] = dynamic_resid(y, x, params, steady_state)
T = NaN(5, 1);
lhs = NaN(12, 1);
rhs = NaN(12, 1);
T(1) = (1+params(2))/(params(5)+params(2)+(1-params(5))*params(1));
T(2) = (1-params(8))*(1-params(4)*params(8))/(params(8)*(1+params(2)*params(7)));
T(3) = (-1)/params(1);
T(4) = (1-params(5))/(1-params(5)+params(5)*params(6))*(1-params(3))*(1-params(3)*params(4))/params(3);
T(5) = T(2)*(params(1)+params(2)/(1-params(5)));
lhs(1) = y(13);
rhs(1) = params(4)*y(25)+y(15)*params(5)*T(4)/(1-params(5))+y(18)*T(4);
lhs(2) = y(14);
rhs(2) = params(4)*y(26)+y(15)*T(5)-T(2)*y(18);
lhs(3) = y(15);
rhs(3) = T(3)*(y(21)-y(25)-y(22))+y(27);
lhs(4) = y(15);
rhs(4) = y(16)-T(1)*y(23);
lhs(5) = y(16);
rhs(5) = y(23)+(1-params(5))*y(17);
lhs(6) = y(18);
rhs(6) = y(19)-y(20);
lhs(7) = y(20);
rhs(7) = y(23)*(1-params(5)*T(1))/(1-params(5));
lhs(8) = y(18);
rhs(8) = y(14)+y(6)-y(13)-(y(20)-y(8));
lhs(9) = y(22);
rhs(9) = y(23)*T(1)*(-params(1))*(1-params(9));
lhs(10) = y(21);
rhs(10) = y(13)*params(11)+params(12)*y(24)+params(10)*y(9)+x(1);
lhs(11) = y(24);
rhs(11) = y(16)-(steady_state(4));
lhs(12) = y(23);
rhs(12) = params(9)*y(11)+x(2);
end
