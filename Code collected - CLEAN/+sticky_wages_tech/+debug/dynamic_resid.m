function [lhs, rhs] = dynamic_resid(y, x, params, steady_state)
T = NaN(5, 1);
lhs = NaN(13, 1);
rhs = NaN(13, 1);
T(1) = (1+params(2))/(params(5)+params(2)+(1-params(5))*params(1));
T(2) = (1-params(8))*(1-params(4)*params(8))/(params(8)*(1+params(2)*params(7)));
T(3) = (-1)/params(1);
T(4) = (1-params(5))/(1-params(5)+params(5)*params(6))*(1-params(3))*(1-params(3)*params(4))/params(3);
T(5) = T(2)*(params(1)+params(2)/(1-params(5)));
lhs(1) = y(14);
rhs(1) = params(4)*y(27)+y(16)*params(5)*T(4)/(1-params(5))+y(19)*T(4);
lhs(2) = y(15);
rhs(2) = params(4)*y(28)+y(16)*T(5)-T(2)*y(19);
lhs(3) = y(16);
rhs(3) = T(3)*(y(26)-y(27)-y(23))+y(29);
lhs(4) = y(16);
rhs(4) = y(17)-T(1)*y(24);
lhs(5) = y(17);
rhs(5) = y(24)+(1-params(5))*y(18);
lhs(6) = y(19);
rhs(6) = y(20)-y(21);
lhs(7) = y(21);
rhs(7) = y(24)*(1-params(5)*T(1))/(1-params(5));
lhs(8) = y(19);
rhs(8) = y(15)+y(6)-y(14)-(y(21)-y(8));
lhs(9) = y(23);
rhs(9) = y(24)*T(1)*(-params(1))*(1-params(9));
lhs(10) = y(26);
rhs(10) = y(14)*params(11)+params(12)*y(25)+y(22);
lhs(11) = y(22);
rhs(11) = params(10)*y(9)+x(1);
lhs(12) = y(25);
rhs(12) = y(17)-(steady_state(4));
lhs(13) = y(24);
rhs(13) = params(9)*y(11)+x(2);
end
